package org.albianj.cached.service;

public class AlbianCachedAttributeException extends RuntimeException {
	/**
	 * 
	 */

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 
	 */

	public AlbianCachedAttributeException() {
		super();
	}

	/**
	 * @param msg
	 */
	public AlbianCachedAttributeException(String msg) {
		super(msg);
	}

	/**
	 * @param msg
	 * @param cause
	 */
	public AlbianCachedAttributeException(String msg, Throwable cause) {
		super(msg, cause);
	}

	/**
	 * @param cause
	 */
	public AlbianCachedAttributeException(Throwable cause) {
		super(cause);
	}
}
